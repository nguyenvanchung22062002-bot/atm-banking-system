
CREATE DATABASE IF NOT EXISTS atm_db CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE atm_db;

CREATE TABLE IF NOT EXISTS accounts (
    id          BIGINT PRIMARY KEY AUTO_INCREMENT,
    card_number VARCHAR(16) UNIQUE NOT NULL,
    owner_name  VARCHAR(100) NOT NULL,
    pin_hash    VARCHAR(255) NOT NULL,
    balance     DECIMAL(15,2) NOT NULL DEFAULT 0.00,
    is_locked   TINYINT(1) DEFAULT 0,
    created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);


CREATE TABLE IF NOT EXISTS transactions (
    id              BIGINT NOT NULL AUTO_INCREMENT,
    account_id      BIGINT NOT NULL,
    type            ENUM('DEPOSIT','WITHDRAW') NOT NULL,
    amount          DECIMAL(15,2) NOT NULL,
    balance_after   DECIMAL(15,2) NOT NULL,
    status          ENUM('SUCCESS','FAILED') NOT NULL,
    note            VARCHAR(255),
    created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id, created_at)
)
PARTITION BY RANGE (YEAR(created_at) * 100 + MONTH(created_at)) (
    PARTITION p202401 VALUES LESS THAN (202402),
    PARTITION p202402 VALUES LESS THAN (202403),
    PARTITION p202403 VALUES LESS THAN (202404),
    PARTITION p202404 VALUES LESS THAN (202405),
    PARTITION p202405 VALUES LESS THAN (202406),
    PARTITION p202406 VALUES LESS THAN (202407),
    PARTITION p202407 VALUES LESS THAN (202408),
    PARTITION p202408 VALUES LESS THAN (202409),
    PARTITION p202409 VALUES LESS THAN (202410),
    PARTITION p202410 VALUES LESS THAN (202411),
    PARTITION p202411 VALUES LESS THAN (202412),
    PARTITION p202412 VALUES LESS THAN (202501),
    PARTITION p202501 VALUES LESS THAN (202502),
    PARTITION p202502 VALUES LESS THAN (202503),
    PARTITION p202503 VALUES LESS THAN (202504),
    PARTITION p202504 VALUES LESS THAN (202505),
    PARTITION p202505 VALUES LESS THAN (202506),
    PARTITION p202506 VALUES LESS THAN (202507),
    PARTITION p202507 VALUES LESS THAN (202508),
    PARTITION p202508 VALUES LESS THAN (202509),
    PARTITION p202509 VALUES LESS THAN (202510),
    PARTITION p202510 VALUES LESS THAN (202511),
    PARTITION p202511 VALUES LESS THAN (202512),
    PARTITION p202512 VALUES LESS THAN (202601),
    PARTITION p202601 VALUES LESS THAN (202602),
    PARTITION p202602 VALUES LESS THAN (202603),
    PARTITION p202603 VALUES LESS THAN (202604),
    PARTITION p_future VALUES LESS THAN MAXVALUE
);


CREATE TABLE IF NOT EXISTS report_monthly (
    id               BIGINT PRIMARY KEY AUTO_INCREMENT,
    account_id       BIGINT NOT NULL,
    year_month       CHAR(7) NOT NULL,
    total_deposit    DECIMAL(15,2) DEFAULT 0.00,
    total_withdraw   DECIMAL(15,2) DEFAULT 0.00,
    tx_count         INT DEFAULT 0,
    updated_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    UNIQUE KEY uq_account_month (account_id, year_month)
);


CREATE INDEX idx_tx_account_date ON transactions(account_id, created_at);
CREATE INDEX idx_tx_status       ON transactions(status, created_at);

